class safar{
	public static void main(String[]args){
		/*deklarasi variabel*/
		short a=1;
		int b=100;
		long c=1890;
		char d=30;
		char e='R';
		double f=40.2d;
		float g=40.2f;
		
		System.out.println("karakter ="+e);//karakter sebagai karakter
		System.out.println("karakter ="+d);//karakter sebagai integer
		System.out.println("Bilangan integer (short) ="+a);//bilangan bulat dengan tipe data short
		System.out.println("Bilangan integer (int) ="+b);//bilangan bulat dengan tipe data int
		System.out.println("Bilangan integer (long) ="+c);//bilangan bulat dengan tipe data long
		System.out.println("Bilangan real f ="+f); //bilangan real dengan tipe data double
		System.out.println("Bilangan real g ="+g);//bilangan real dengan tipe data float
		
	}
}